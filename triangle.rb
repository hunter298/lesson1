#Программа вычисляющая площадь треугольника
#Thinknetica курс Профессиональная разработка на RoR
#Урок 1

puts "Введите высоту и площадь основания треугольника\n"

print 'Основание    а = '
a = STDIN.gets.chomp.to_f


print 'Высота       h = '
h = STDIN.gets.chomp.to_f


puts 'Площадь треугольника равна:'
puts "(#{a} * #{h}) / 2 = #{a * h / 2}"